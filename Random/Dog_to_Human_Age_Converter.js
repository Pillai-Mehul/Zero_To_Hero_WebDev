// 1. Collect the dog's age from the user
var dogAge = prompt("What is your Dog Age:");

// 2. Apply the conversion formula:
// Subtract the first 2 years, multiply by 4, then add 21 (the value of the first 2 years)
var humanAge = (dogAge - 2) * 4 + 21;

// 3. Display the result
alert("Your Dog's Age in human years is: " + humanAge);
